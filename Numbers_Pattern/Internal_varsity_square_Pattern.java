package Numbers_Pattern;
import java.util.Scanner;
public class Internal_varsity_square_Pattern {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        for(int i=0;i<=n;i++){
            System.out.print("3");
            System.out.print(i+1);
            System.out.println("3");
        }
        System.out.println("");
    }
}
